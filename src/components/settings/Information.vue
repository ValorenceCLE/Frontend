This will be the component to hold all of the system Information It will have a small
Header style card that will say "Information" Below that it will need one main container
that is sized/fitted to the screen Within that container we will need three more
containers that represent: Pi Information Router Information Camera Information Each of
those should have a header that says the name of the device Below that should be a list of
information about the device Pi Information: - IP Address - MAC Address - Uptime - CPU
Usage - Memory Usage - Disk Usage Router Information: - IP Address - Serial Number -
Uptime - Connectivity - Status Camera Information: - IP Address - Serial Number - Uptime -
Connectivity - Status
